<h1>PARLIAMENTARY ELECTIONS</h1>

<h2>SEJM</h2>

<ul class="list-group list-group-flush">
	<li class="list-group-item">The term of office of the Sejm and Senate is 4 years.</li>
	<li class="list-group-item">You cannot run for the Sejm and Senate at the same time.</li>
	<li class="list-group-item">Voting lasts one day and we elect 100 senators and 460 deputies.</li>
	<li class="list-group-item">
		Candidates may be nominated by electoral committees. They also lead election campaign for
		candidates. Committees may be in turn created by political parties, coalitions of these parties
		or by voters. Each of them appoints an authorized electoral representative to act on his behalf
		and on his behalf. The attorney has obligation to notify the relevant electoral authority about
		its establishment election committee, coalition election committee, respectively, voters'
		election committee
	</li>
</ul>

<h2>SENATE</h2>
<ul class="list-group list-group-flush">
	<li class="list-group-item">
		100 senators out of 100 are elected to the Senate single-member districts. Every committee Only
		1 candidate can be nominated in a given election district, the voter also votes for 1 candidate.
	</li>
	<li class="list-group-item">
		The senator's mandate is obtained by the candidate who received it the largest number of votes
		(majority rule).
	</li>
	<li class="list-group-item">
		Single-member districts in the Senate elections remained introduced in 2011. Previously,
		senators were elected according to the majority system with 40 districts multi-member (from 2 to
		4 seats).
	</li>
</ul>

<h2>PRESIDENTIAL ELECTIONS</h2>

<ul class="list-group list-group-flush">
	<li class="list-group-item">
		The candidate who receives it is elected President absolute majority of valid votes cast. IN in
		the event that none of the candidates receives the required majority, there is a second round of
		voting.
	</li>
	<li class="list-group-item">
		The second round takes place 14 days after the first vote. Only the two candidates who received
		the highest number of votes in the first ballot take part in this round. In a situation where
		one of the candidates resigns, dies or loses the right to vote, his place is taken by the
		candidate who obtained the largest number of votes. In this case, the second round is postponed
		for another 14 days.
	</li>
	<li class="list-group-item">
		He is elected President of the Republic of Poland candidate who received the re-vote more votes.
	</li>
	<li class="list-group-item">
		The president begins his term of office from submission to the National Assembly oaths.
	</li>
</ul>
